char neighborsC(char f[1000], unsigned int r, unsigned int c){
    char result=0;

    if ((r!= 0)&&(c!= 0)) result+=f[index(r-1,c-1)];

    if  (r!= 0)           result+=f[index(r-1,c  )];

    if ((r!= 0)&&(c!=49)) result+=f[index(r-1,c+1)];

    if           (c!= 0)  result+=f[index(r  ,c-1)];

    if           (c!=49)  result+=f[index(r  ,c+1)];

    if ((r!=19)&&(c!= 0)) result+=f[index(r+1,c-1)];

    if  (r!=19)           result+=f[index(r+1,c  )];

    if ((r!=19)&&(c!=49)) result+=f[index(r+1,c+1)];

    return result;
}



void newGenC(char f[1000], char n[1000])
{
    // calculate neighbors for each cell
    unsigned int r, c;
    for (r=0; r<20; r++){
        for (c=0; c<50; c++)
            n[index(r,c)] = neighborsC(f,r,c); // replace by neighbors(f,r,c) for mixing C and asm
    }
    // apply rules for each cell
    for (r=0; r<20; r++){

        for (c=0; c<50; c++){

            switch (n[index(r,c)]){
            case 2:
                // nothing to do, live cells stay alive, dead cells stay dead
                break;
            case 3:
                // alive, either as newborn or staying alive
                f[index(r,c)]=1;
                break;
            default:
                // die (less than 2 or more than 3 neighbors)
                f[index(r,c)]=0;
            }
        }
    }

}


